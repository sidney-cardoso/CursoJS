// ----------------------- Operadores de Comparação ----------------------- //

/**
 * > Maior que
 * < Menor que
 * >= Maior ou igual
 * <= Menor ou igual
 * == Igual a ( checa se o valor é igual a outro valor )
 * != Diferente de ( checa se o valor é diferente de outro valor )
 * === Estritamente Igual ( checa se o valor é igual a outro valor e o tipo também )
 * !== Estritamente diferente ( checa se o valor é diferente de outro valor e o tipo também )
 */
// É recomendado utilizarmos sempre igualdade e diferença estrita para evitar erros na hora de executar o código 

let a = 10
let b = 20
let c = '10'
let d = '20'
console.log(b > a)
console.log(b >= d)
console.log(b <= d)
console.log(a == c)
console.log(b != d)
console.log(a === c)
console.log(b !== d)
